    private void choiceMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choiceMenuActionPerformed
        // TODO add your handling code here:
        switch (choiceMenu.getSelectedIndex()){
            case 0: 
                setCheckBox(tweetAnalyse);
                selection = "Tweets";
                break;
            case 1:   
                setCheckBox(zooAnalyse);
                selection = "Zoo";
                break;
            case 2: 
                setCheckBox(animalAnalyse);
                selection = "Animal";
                break;
            case 3:
                setCheckBox(weatherAnalyse);
                selection = "Weather";
                break;       
        }
    }//GEN-LAST:event_choiceMenuActionPerformed

    private void choiceBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choiceBox2ActionPerformed
        switch (selection){
            case "Tweets":
                    switch (choiceBox2.getSelectedIndex()){
                        case 0: 
                            
                            createChart = new CreateChart(query.getBestAsset(), "Most popular asset"); 
                            break;
                        case 1:
                            createChart = new CreateChart(query.getWorstAsset(), "Least popular asset");
                            break;
                        case 2:
                            createChart = new CreateChart(query.getCountry(), "Country");
                        
                        case 3: 
                            createChart = new CreateChart(query.getRegion(), "Region");
                    }
                break;
            case "Animal":
                    switch (choiceBox2.getSelectedIndex()){
                        case 0: 
                            createChart = new CreateChart(query.getBestAnimal(), "Tweet about animals"); 
                            break;
                    }
                break;
            case "Zoo":
                    switch (choiceBox2.getSelectedIndex()){
                        case 0: 
                            createChart = new CreateChart(query.getBlijdorpTweetsAndDate(), "Response"); 
                            break;
                        case 1:
                            createChart = new CreateChart(query.getBlijdorpTweets(), "Tweet content");
                            break;
                        case 2:
                            createChart = new CreateChart(query.getFollowersBlijdorp(), "Followers");
                            break;
                    }
                break;
            case "Weather":
                        switch (choiceBox2.getSelectedIndex()){
                            case 0: 
                                createChart = new CreateChart(query.getRainAndTweets(), "Rain"); 
                                break;
                            case 1:
                                createChart = new CreateChart(query.getGoodWeatherAndTweets(), "Sun");
                                break;
                            case 2:
                                createChart = new CreateChart(query.getLowTemperatureAndTweets(), "Low temperature");
                                break;
                            case 3:
                                createChart = new CreateChart(query.getHighTemperatureAndTweets(), "High temperature");
                                break;    
                        }
                break;
        }//end of (selection) switch
            
            
            
        
    }//GEN-LAST:event_choiceBox2ActionPerformed


